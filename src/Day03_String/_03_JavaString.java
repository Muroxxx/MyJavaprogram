package Day03_String;

public class _03_JavaString {
    public static void main(String[] args) {

        String ad = "İsmet"; // String : kelime veya kelimeleri saklamak için kullanılır, sınırlarını " " ile belirler.

        String soyad = "Temur";

        System.out.println("ad = " + ad);
        System.out.println("soyad = " + soyad);
        System.out.println("ad ve soyad = " + ad + "\t" + soyad);
        // + lar burada stringler arasında olduğu birleştirir


    }
}
